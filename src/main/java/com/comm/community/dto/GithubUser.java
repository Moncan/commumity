package com.comm.community.dto;

public class GithubUser {
    private Long id;
    private String name;
    private String bio;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDio() {
        return bio;
    }

    public void setDio(String dio) {
        this.bio = dio;
    }
}
